#include <iostream>
#include <cmath>
using namespace std;
int main(){
	//abs函数
	cout<<abs(-2)<<endl;
	cout<<abs(2)<<endl;
	cout<<abs(-3.14)<<endl;
	cout<<abs(3.14)<<endl;
	
	//pow函数
	cout<<pow(2,3)<<endl;
	cout<<pow(4,2)<<endl;
	cout<<pow(3,3)<<endl;
	
	//sqrt函数
	cout<<sqrt(4)<<endl;
	cout<<sqrt(9)<<endl;
	cout<<sqrt(81)<<endl;
	return 0;
}
